#CLASSES
class World():
	def __init__(self, data):
		self.tile_list = []

		dirt = pygame.image.load('Assets/brown.png')
		grass = pygame.image.load('Assets/green.png')

		row_count = 0
		for row in data:
			col_count = 0
			for tile in row:
				#Dirt Tiles
				if tile == 1:
					img = pygame.transform.scale(dirt, (TILE_SIZE, TILE_SIZE))
					img_rect = img.get_rect()
					img_rect.x = col_count * TILE_SIZE
					img_rect.y = row_count * TILE_SIZE
					tile = (img, img_rect)
					self.tile_list.append(tile)
				#Grass Tiles	
				elif tile == 2:
					img = pygame.transform.scale(grass, (TILE_SIZE, TILE_SIZE))
					img_rect = img.get_rect()
					img_rect.x = col_count * TILE_SIZE
					img_rect.y = row_count * TILE_SIZE
					tile = (img, img_rect)
					self.tile_list.append(tile)
				col_count += 1
			row_count += 1

	def draw(self):
		for tile in self.tile_list:
			screen.blit(tile[0], tile[1])

#OTHER FUNCTIONS
def grid():
	for line in range(0, TILES_AMOUNT):
		pygame.draw.line(screen, WHITE, (0, line * TILE_SIZE), (SCREEN_WIDTH, line * TILE_SIZE))
		pygame.draw.line(screen, WHITE, (line * TILE_SIZE, 0), (line * TILE_SIZE, SCREEN_HEIGHT))


   
